    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // PatrÃ³n para validar el email
        Pattern pattern = Pattern
                .compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                        + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
 
        // El email a validar
        String email = txtRecibe.getText();
 
        Matcher mather = pattern.matcher(email);
        if ((this.txtRecibe.getText().isEmpty())
                        || (this.txtAsunto.getText().isEmpty())
                || (this.txtMensaje.getText().isEmpty())
                || (mather.find()==false)){
        
            JOptionPane.showMessageDialog(null, "Llene los campos de informacion correctamente", "Error", JOptionPane.ERROR_MESSAGE);
        
    
 
       
            
            
        } else {
            
            // TODO add your handling code here:
        
        Properties propiedad = new Properties();
        propiedad.setProperty("mail.smtp.host", "smtp.gmail.com");
        propiedad.setProperty("mail.smtp.starttls.enable", "true");
        propiedad.setProperty("mail.smtp.port", "587");
        propiedad.setProperty("mail.smtp.user", "aguacalumajava@gmail.com");
        propiedad.setProperty("mail.smtp.clave", "Babahoyo2019");    //La clave de la cuenta
        propiedad.setProperty("mail.smtp.starttls.enable", "true");
        propiedad.setProperty("mail.smtp.auth", "true");
        propiedad.setProperty("mail.smtp.ssl.trust", "*"); 
        
        Session sesion = Session.getDefaultInstance(propiedad);
        
        String correoEnvia = "aguacalumajava@gmail.com";
        String contrasena = "Babahoyo2019";
        String destinatario = txtRecibe.getText();
        String asunto = txtAsunto.getText();
        String mensaje = txtMensaje.getText();
        
        
        MimeMessage mail = new MimeMessage(sesion);
        
        try {
            mail.setFrom(new InternetAddress (correoEnvia));
            mail.addRecipient(Message.RecipientType.TO, new InternetAddress(destinatario));
            mail.setSubject(asunto);
            mail.setText(mensaje);
            
            
            Transport transporte = sesion.getTransport("smtp");
            transporte.connect(correoEnvia,contrasena);
            transporte.sendMessage(mail, mail.getRecipients(Message.RecipientType.TO));
            transporte.close();
            
            JOptionPane.showMessageDialog(null, "Correo enviado");
            
            
            
            
            
        } catch (AddressException ex) {
            JOptionPane.showMessageDialog(null, "Error");
           
            System.out.println(ex);
        } catch (MessagingException ex) {
            JOptionPane.showMessageDialog(null, "Error");
            System.out.println(ex);
        }
        
        
        
        
            
        }

        
        
    }//GEN-LAST:event_jButton1ActionPerformed

