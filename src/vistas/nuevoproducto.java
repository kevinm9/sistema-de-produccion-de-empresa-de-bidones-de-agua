    private void TXTNOMBRE_PRODUCTOKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TXTNOMBRE_PRODUCTOKeyTyped
      
       char c = evt.getKeyChar();
        if (!Character.isLetter(c) && !(c == KeyEvent.VK_SPACE) && !(c == KeyEvent.VK_BACK_SPACE)) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }   if (this.TXTNOMBRE_PRODUCTO.getText().length() >= 20) {
            evt.consume();

            JOptionPane.showMessageDialog(null, "Limite maximo de 20 caracteres", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
 
    }//GEN-LAST:event_TXTNOMBRE_PRODUCTOKeyTyped

