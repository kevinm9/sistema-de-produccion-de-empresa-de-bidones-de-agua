    private void btguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btguardarActionPerformed

        try {
            if (conexion.getConexion() != null) {

                //declaramos a las variables los valores de los textbox
                
                String VTXTCODIGO_PRODUCTO = this.TXTCODIGO_PRODUCTO.getText();
                String VTXTNOMBRE_PRODUCTO = this.TXTNOMBRE_PRODUCTO.getText();
                
                
                DecimalFormatSymbols separadoresPersonalizados = new DecimalFormatSymbols();
                 separadoresPersonalizados.setDecimalSeparator('.');
                 DecimalFormat df = new DecimalFormat("#.##",separadoresPersonalizados);
                
                
                String VTXTCOSTO_PRODUCTO = df.format(Double.parseDouble(TXTCOSTO_PRODUCTO.getText()));
             
                 String VTXTPRECIO_PRODUCTO = df.format(Double.parseDouble(TXTPRECIO_PRODUCTO.getText()));
             
                
                String VTXTMATERIAL_PRODUCTO = this.TXTMATERIAL_PRODUCTO.getText();
                String VTXTPRESENTACION_PRODUCTO = this.TXTPRESENTACION_PRODUCTO.getText();
                
                
                String VTXTPESO_PRODUCTO = df.format(Double.parseDouble(TXTPESO_PRODUCTO.getText()));
             
                String VTXTCAPACIDAD_PRODUCTO = df.format(Double.parseDouble(TXTCAPACIDAD_PRODUCTO.getText()));
             
                
                String VTXTDESCRIPCION_PRODUCTO = this.TXTDESCRIPCION_PRODUCTO.getText();

                //finalizamo la declaracion a las variables los valores de los textbox

                if ((this.TXTCODIGO_PRODUCTO.getText().isEmpty())
                    || (this.TXTNOMBRE_PRODUCTO.getText().isEmpty())
                    || (this.TXTCOSTO_PRODUCTO.getText().isEmpty())
                    || (this.TXTPRECIO_PRODUCTO.getText().isEmpty())
                    || (this.TXTMATERIAL_PRODUCTO.getText().isEmpty())
                    || (this.TXTPRESENTACION_PRODUCTO.getText().isEmpty())
                    || (this.TXTPESO_PRODUCTO.getText().isEmpty())
                    || (this.TXTCAPACIDAD_PRODUCTO.getText().isEmpty())
                    || (this.TXTDESCRIPCION_PRODUCTO.getText().isEmpty())
                ) {
                    //incorrecto
                    JOptionPane.showMessageDialog(null, "Ingrese correctamente los datos o llene los campos vacios", "Error", JOptionPane.ERROR_MESSAGE);

                } else {

                    conexion cn = new conexion();

                    //asiganmos la variables a los set del objeto
                    producto.setCODIGO_PRODUCTO(VTXTCODIGO_PRODUCTO);
                    producto.setNOMBRE_PRODUCTO(VTXTNOMBRE_PRODUCTO);
                    producto.setCOSTO_PRODUCTO(VTXTCOSTO_PRODUCTO);
                    producto.setPRECIO_PRODUCTO(VTXTPRECIO_PRODUCTO);
                    producto.setMATERIAL_PRODUCTO(VTXTMATERIAL_PRODUCTO);
                    producto.setPRESENTACION_PRODUCTO(VTXTPRESENTACION_PRODUCTO);
                    
                    producto.setPESO_PRODUCTO(VTXTPESO_PRODUCTO);
                    producto.setCAPACIDAD_PRODUCTO(VTXTCAPACIDAD_PRODUCTO);
                    producto.setDESCRIPCION_PRODUCTO(VTXTDESCRIPCION_PRODUCTO);

                    //insertamos el objeto en la base de datos
                    productosql.insertarproducto(producto);

                    limpiarcampos();

                }

            } else {
                //no conexion
                JOptionPane.showMessageDialog(null, "Error en Conexión PostgreSQL 9.0 ", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            //vacio
            JOptionPane.showMessageDialog(null, "No existen datos veriifique la informacion ingresada que se correcta", "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex.getMessage());
        }

    }//GEN-LAST:event_btguardarActionPerformed

